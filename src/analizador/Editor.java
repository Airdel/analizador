    private ArrayList<String> leerArchivo(String file) {
        ArrayList<String> texto = null;
        FileReader fr = null;
        BufferedReader br = null;
        String line = "";
        try {
            texto = new ArrayList<String>();
            archivo = new File(file);
            String nombre = archivo.getName();
            setTitle(nombre);
            if (archivo.exists()) {
                if (archivo.isFile()) {
                    fr = new FileReader(archivo);
                    br = new BufferedReader(fr);
                    line = br.readLine();
                    while (line != null) {
                        if (!line.equalsIgnoreCase("")) {
                            texto.add(line);
                        }
                        line = br.readLine();
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return texto;
    }

    public void abrirArchivo() {
        ArrayList<String> linea = null;
        int resp = 0;
        String codigo = "";
        txtEditor.setText("");
        resp = jFileChooser1.showOpenDialog(this);
        if (resp == JFileChooser.APPROVE_OPTION) {
            linea = leerArchivo(jFileChooser1.getSelectedFile().toString());
            if (linea.size() > 0) {
                for (int i = 0; i < linea.size(); i++) {
                    codigo = (String) linea.get(i);
                    txtEditor.append(codigo + "\n");

                }
            }
            scrollEditor.setBounds(scrollEditor.getX(), scrollEditor.getY(), 570, 350);
            txtEditor.setBounds(txtEditor.getX(), txtEditor.getY(), 570, 350);
            lbCaracteres.setText(contarCar() + "");
            lbPalabras.setText(contarPal() + "");
            guardarComo = false;
        }
    }

    private int contarCar() {
        String cad = txtEditor.getText().replace("\n", "");
        cad = cad.replace(" ", "");
        return cad.length();
    }

    private int contarPal() {
        String cad = txtEditor.getText().replace("\n", " ");
        String A[] = cad.split(" ");
        int p = 0;
        for (int i = 0; i < A.length; i++) {
            if (!A[i].equals("")) {
                p++;
            }
        }
        return p;
    }

    private void guardar() {
        FileWriter save = null;
        try {
            save = new FileWriter(archivo.getAbsolutePath());
            save.write(txtEditor.getText());
            save.close();
        } catch (IOException ex) {
            Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                save.close();
            } catch (IOException ex) {
                Logger.getLogger(Editor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    public void guardarComo() {
        try {
            JFileChooser file = new JFileChooser();
            if (file.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
                archivo = file.getSelectedFile();
                if (archivo != null) {
                    FileWriter save = new FileWriter(archivo.getAbsolutePath() + ".lya");
                    String nombre = archivo.getName();
                    setTitle(nombre);
                    save.write(txtEditor.getText());
                    save.close();
                    guardarComo = false;
                }
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,
                    "Su archivo no se ha guardado",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }
